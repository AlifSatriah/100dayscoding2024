package day8;


public class Day8 {

    
    public static void main(String[] args) {
        // mendeklarasikan dan menginisialisasi variabel
        String nama = "alif satria";
        int angka = 20;
        //mencetak nilai awal
        System.out.println("nama : "+ nama);
        System.out.println("angka :"+ angka);
        
        //mengupdate nilai variabel
        nama ="aco satria";
        angka =30;
        //mencetak nilai setelah update
        System.out.println("nama setelah update :" + nama);
        System.out.println("nama setelah update :" + angka);
    }
    
}
