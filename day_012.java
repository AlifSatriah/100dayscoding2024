package day12;


public class Day12 {

    
    public static void main(String[] args) {
        //baris pertama (Bilangan Ganjil)- deklarasi variabel
        int apel, jeruk, mangga, pisang, anggur, semangka;
        //inisialisasi variabel
        apel = 7;
        jeruk = 5;
        mangga = 9;
        pisang = 1;
        anggur = 3;
        semangka = 27;
        System.out.printf("Bilangan Ganjil = %d, %d, %d, %d, %d, %d\n ", apel, jeruk, mangga, pisang, anggur, semangka);
        
        //baris kedua (Bilangan Genap)
        int durian = 8;
        int nanas = 10;
        int rambutan = 4;
        int alpukat = 100;
        System.out.println("Bilangan Genap = " + durian + "," + nanas + "," + rambutan + "," + alpukat);
        
        //baris ketiga (Bilangan Prima)
        int jambu = 13;
        int belimbing = 23;
        int sirsak = 5;
        int pepaya = 11;
        System.out.printf("Bilangan Prima = %d, %d, %d, %d\n", jambu, belimbing, sirsak, pepaya);
        
    }
    
}
